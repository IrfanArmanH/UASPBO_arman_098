    private void autonumber(){
        try{
            Connection c = koneksi.getKoneksi();
            Statement s = (Statement) c.createStatement();
            String sql = "SELECT * FROM tbsepatu ORDER BY idSepatu DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()){
                String NoBarang = r.getString("idSepatu").substring(2);
                String BR = "" + (Integer.parseInt(NoBarang) + 1);
                String Nol = "";
                
                if(BR.length()==1){
                    Nol = "00";
                }else if(BR.length()==2){
                    Nol = "0";
                }else if(BR.length()==3){
                    Nol = "";
                }
                
                txtId.setText("BR" + Nol + BR);
                
            }else{
                txtId.setText("BR001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
    
